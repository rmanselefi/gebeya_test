     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user_id=auth()->user()->id;
        $orders=null;
        $ordprod=OrderProduct::where('user_id',$user_id)->get();
        $categories=Category::all();
        foreach ($ordprod as $ord) {
            # code...
            $user_idd=$ord->order_id;
            if($orders!=null){
                $orders=$orders->merge(Order::where('id',$user_idd)->get());
            }
            else{
                $orders=Order::where('id',$user_idd)->get();
            }
        }
        return view('vendor_orders')->with([
            'orders'=>$orders,
            'categories'=>$categories
        ]);
    }

